interface PanelSidebarProps {
  tenantName: string;
  companyName: string;
}

export function PanelSidebar({ tenantName, companyName }: PanelSidebarProps) {
  const pathname = usePathname();
  const router = useRouter();
  const [mobileOpen, setMobileOpen] = useState(false);

  async function handleLogout() {
    await fetch("/api/auth/logout", { method: "POST" });
    router.push("/login");
  }

  function isActive(href: string) {
    if (href === "/panel/products/new") {
      return pathname === href;
    }
    if (href === "/panel/dashboard") {
      return pathname === href;
    }
    if (href === "/panel/products") {
      return pathname === href;
    }
    return pathname.startsWith(href);
  }

  const sidebar = (
    <aside className="fixed top-0 left-0 h-full w-[260px] bg-white border-r border-gray-200 flex flex-col z-40">
      {/* Branding */}
      <div className="px-6 py-5 border-b border-gray-100">
        <h1 className="text-lg font-bold text-gray-900">Tano Atelier</h1>
        <p className="text-xs text-blue-600 font-medium mt-0.5">Bayi Paneli</p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 px-3 space-y-1 overflow-y-auto">
        {NAV_ITEMS.map((item) => (
          <Link
            key={item.href}
            href={item.href}
            onClick={() => setMobileOpen(false)}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm transition-colors ${
              isActive(item.href)
                ? "bg-blue-50 text-blue-700 font-medium"
                : "text-gray-600 hover:bg-gray-50 hover:text-gray-900"
            }`}
          >
            <span className={isActive(item.href) ? "text-blue-600" : "text-gray-400"}>
              {item.icon}
            </span>
            {item.label}
            {"badge" in item && item.badge && (
              <span className="ml-auto w-2 h-2 rounded-full bg-blue-500" />
            )}
          </Link>
        ))}
      </nav>

      {/* Footer */}
      <div className="border-t border-gray-100 px-3 py-4">
        <div className="px-3 mb-3">
          <p className="text-sm font-medium text-gray-900 truncate">{companyName}</p>
          <p className="text-xs text-gray-400 truncate">{tenantName}</p>
        </div>
        <button
          onClick={handleLogout}
          className="flex items-center gap-3 w-full px-3 py-2.5 rounded-lg text-sm text-gray-500 hover:bg-gray-50 hover:text-gray-700 transition-colors"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Çıkış Yap
        </button>
      </div>
    </aside>
  );

  return (
    <>
      {/* Mobile hamburger */}
      <button
        onClick={() => setMobileOpen(!mobileOpen)}
        className="md:hidden fixed top-4 left-4 z-50 p-2 bg-white border border-gray-200 text-gray-700 rounded-lg shadow-sm"
        aria-label="Menu"
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={2}
            d={mobileOpen ? "M6 18L18 6M6 6l12 12" : "M4 6h16M4 12h16M4 18h16"}
          />
        </svg>
      </button>

      {/* Desktop sidebar */}
      <div className="hidden md:block">{sidebar}</div>

      {/* Mobile sidebar + overlay */}
      {mobileOpen && (
        <>
          <div
            className="md:hidden fixed inset-0 bg-black/30 z-30"
            onClick={() => setMobileOpen(false)}
          />
          <div className="md:hidden">{sidebar}</div>
        </>
      )}
    </>
  );
}
